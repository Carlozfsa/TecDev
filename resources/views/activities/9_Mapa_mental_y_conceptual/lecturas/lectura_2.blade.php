                <strong><p style="text-align: center;">MAPA MENTAL Y MAPA CONCEPTUAL</p></strong><br>

                Una vez que se han determinado las ideas ordenadoras básicas se requiere
                considerar otros aspectos que a continuación se presentan
                Aspectos o elementos básicos.<br><br>

                <ol>
                    <li>
                        <strong>Organización:</strong>
                        El mapa debe estar organizado en forma deliberada y la
                        información relacionada con su tópico de origen (partiendo de la idea
                        principal, se conectan nuevas ideas hasta completar la información).
                    </li>
                    <br>

                    <li>
                        <strong>Agrupamiento:</strong>
                        Luego de tener un centro definido, un mapa mental se debe
                        agrupar y expandir a través de la formación de sub-centros que partan de él y
                        así sucesivamente
                    </li>
                    <br>

                    <li>
                        <strong>Imaginación:</strong>
                        Las imágenes visuales son más recordadas que las palabras,
                        por este motivo el centro debe ser una imagen visual fuerte para que todo lo
                        que está en el mapa mental se pueda asociar con él.
                    </li>
                    <br>

                    <li>
                        <strong>Uso de palabras claves:</strong>
                        Las notas con palabras claves son más efectivas
                        que las oraciones o frases, siendo más fácil para el cerebro, recordar éstas
                        que un grupo de palabras, frases u oraciones
                    </li>
                    <br>

                    <li>
                        <strong>Uso de colores: </strong>
                        Se recomienda colorear las líneas, símbolos e imágenes,
                        debido a que es más fácil recordarlas que si se hacen en blanco y negro.<br>
                        Símbolos (herramientas de apoyo): Cualquier clase de símbolo que se utilice
                        es válido y pueden ser usados para relacionar y conectar conceptos que
                        aparecen en las diferentes partes del mapa, de igual manera sirven para
                        indicar el orden de imp
                    </li>
                    <br>

                    <li>
                        <strong> Involucrar la conciencia:</strong>
                        La participación debe ser activa y consciente. Si
                        los mapas mentales se convierten en divertidos y espontáneos, permiten
                        llamar la atención, motivando el interés, la creatividad, la originalidad y
                        ayudan a la memoria
                    </li>
                    <br>

                    <li>
                        <strong> Asociación:</strong>
                        Todos los aspectos que se trabajan en el mapa deben ir
                        asociados entre sí, partiendo desde el centro del mismo, permitiendo que las
                        ideas sean recordadas simultáneamente.
                    </li>
                    <br>

                    <li>
                        <strong>Resaltar:</strong>
                        Cada centro debe ser único, mientras más se destaque o resalte la
                        información, ésta se recordará más rápido y fácilmente.
                    </li>
                </ol>

                Requisitos para la elaboración de mapas mentales:<br><br>

                <ol>
                    <li>Debe evitarse la representación lineal</li><br>
                    <li>Se deben considerar estrategias de aprendizaje</li><br>
                    <li> Las ideas deben ser escritas en hojas blancas, aplicando diferentes colores,
                        líneas, flechas, palabras claves, entre ellos otros elementos que permitan
                        recordar con facilidad la idea central y las secundarias.
                    </li><br>
                    <li>El papel debe colocarse en forma horizontal y utilizarse un color distinto para
                        cada grupo de ideas, yéndolas en el sentido de las agujas del reloj dando uso
                        a palabras claves y escribiendo una palabra por línea.
                    </li>
                </ol>

                <br>

                <strong>Procedimiento para elaborar un mapa mental</strong><br><br>

                A partir de un texto y/o la observación de una situación específica se puede diseñar
                un mapa mental las principales recomendaciones a seguir son las siguientes:<br>

                <ol>
                    <li>Revisión, lectura y análisis de la información</li>
                    <li>Aclarar dudas</li>
                    <li>Identificar la idea principal la cual se colocara en el centro de la hoja</li>
                    <li>Utilizar la hoja de forma horizontal</li>
                    <li>Identificar los elementos centrales del texto: Identificar la ideas secundarias,
                        terciarias, etc.… que estén relacionadas y ordénalas de acuerdo a su
                        jerarquía e importancia, puede ser de la más abstracta y general a la más
                        concreta y específica. Estas se colocaran alrededor de la idea principal,
                        evitando amontonarlas.
                    </li>
                    <li>Agregue niveles adicionales a las subtemas, en caso de que quiera
                        profundizar sobre ideas clave.
                    </li>
                    <li>Para darle más importancia a unas ideas que a otras (priorizar), use el sentido
                        de las manecillas del reloj.
                    </li>
                    <li>Establecer las diferencias entre conceptos, objetos y acontecimientos.</li>
                    <li>Establecer relaciones: Relacione la idea central con los subtemas utilizando
                        líneas, flechas, etc... que las unan.
                    </li>
                    <li>Remarque sus ideas encerrándolas en círculos, subrayándolas, poniendo
                        colores, imágenes, símbolos, etc. Use todo aquello que le sirva para
                        diferenciar y hacer más clara la relación entre las ideas.
                    </li>
                    <li>Completar la información e ideas con imágenes que la representen.</li>
                    <li>Utilizar la creatividad, dar importancia al mapa mental y divertirse al mismo
                        tiempo.
                    </li>
                    <li>No limitarse, el mapa puede ser tan amplio como se desee, esto dependerá
                        de lo que se quiera expresar en el mismo.
                    </li>
                    <li>Revisarlo: Comprobar si el mapa es correcto revisándolo detenidamente y
                        reflexionando, si la información se puede interpretar fácilmente y si se
                        pueden encontrar relaciones antes no vistas entre los nodos, entonces se
                        podrá comprobar que el mapa cumple con el objetivo.
                    </li>
                </ol>


