                El <strong>mapa conceptual</strong> es un recurso esquemático para representar un conjunto de
                conceptos y sus relaciones de una manera gráfica, es útil para organizar y
                comunicar lo que se sabe.<br><br>
                Representación grafica de ideas que permite organizar la formación de acuerdo con
                las relaciones causales, niveles de jerarquías o procedimientos.<br><br>
                Los mapas conceptuales son artefactos para la organización y representación del
                conocimiento. Su objetivo es representar relaciones entre conceptos en forma de
                proposiciones.

