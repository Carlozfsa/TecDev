                <strong>El hombre que fue puesto en una jaula</strong><br>
                (Condensado)<br>
                La historia comienza con un rey, quien, una tarde mientras se entregaba a
                ensoñaciones frente a la ventana de su palacio vio a un hombre abajo, en la plaza
                de la ciudad. Aparentemente era un hombre común, que regresaba en la noche a su
                casa por el mismo camino, lo hacía cinco veces a la semana desde hacía muchos
                años.<br>
                Y una repentina curiosidad embargó al rey, que por un memento olvidó su fatiga.
                "Me pregunto que sucederá si se encierra a un hombre en una jaula, como los
                animales en el zoológico".<br>
                Así, al día siguiente, el rey llamó a un psicólogo, le contó su idea, y lo invitó a
                observar el experimento. Cuando el psicólogo objetó, diciendo: "Es algo inconcebible
                meter a un hombre en una jaula': el monarca replicó que muchos gobernantes lo
                hicieron en efecto.<br>
                En ese memento el psicólogo sintió una gran curiosidad acerca de lo que podría
                suceder si se encerraba a un hombre en una jaula.
                La jaula se instaló en un patio privado interno en las instalaciones del palacio, se
                trajo al hombre común que el rey viera desde la ventana de su palacio y se le colocó
                allí dentro.<br>
                Al comienzo, el hombre estaba simplemente perplejo, y decía repetidamente al
                psicólogo: "Debo tomar el tranvía, tengo que ir a trabajar, mire qué hora es, Llegaré
                tarde al trabajo" Pero después, por la tarde el hombre comenzó a darse cuenta, con
                más calma, de lo que sucedía, y entonces protestó vehementemente: "¡El rey no
                puede hacerme esto! Es injusto! !Está en contra de la ley! "<br>
                Su voz era potente y sus ojos estaban llenos de ira. Al psicólogo le gustó el hombre
                por su furia, y vagamente comenzó a darse cuenta de que ésta era una conducta
                que encontraba frecuentemente en la gente con la que trabajaba en su clínica. "Si"
                comprendió, "esta ira es una actitud de la gente que --como los saludables
                adolescentes de cualquier época quiere luchar contra lo que está mal, que protesta
                directamente contra ello. Cuando la gente viene a la clínica de esta manera, es
                bueno, se les puede ayudar".<br>
                Durante el resto de la semana el hombre persistió en sus protestas vehementes.
                Cuando el rey caminaba cerca de la jaula, como lo hacia todos lo días, el hombre
                dirigía sus protestas directamente al monarca.<br>
                Pero él rey le contestaba: "Vea, tiene cantidades de comida, una buena cama, y no
                tiene que trabajar. Lo cuidamos bien, así que ¿de que protesta?<br>
                Después de unos días las protestas del hombre disminuyeron y luego cesaron.
                Estaba silencioso en su jaula negándose a hablar, por lo general. Pero él Psicólogo
                podía ver que el odio brillaba en sus ojos. Cuando cambiaba unas pocas palabras,
                éstas eran breves y definidas, pronunciadas con voz fuerte, vibrante, pero calma
                propia de la persona que odia y sabe a quién odiar.<br>
                Siempre que el rey venía al patio, había un intenso fuego en los ojos del hombre.<br>
                El psicólogo pensó: "Este debe ser el modo en que la gente actúa cuando se le
                vence per primera vez.' Recordó que también había visto esa expresión en los ojos y
                escuchado ese tono de la voz en muchos pacientes de su clínica: El adolescente
                que fue acusado injustamente en su casa o en la escuela y que no puede hacer
                nada, el estudiante universitario de cursos superiores que es consagrado como
                estrella del estadio por el público universitario, pero al que sus profesores le exigen
                aprobar cursos que no puede preparar si es que quiere tener éxito en el futbol, y al
                que luego se le expulsa del colegio por el fracaso que resulta. Y el psicólogo, al
                observar el odio activo en los ojos del hombre, pensó: "aún es bueno; una persona
                que tiene ese deseo de luchar, puede ser ayudada.<br>
                Todos los días el rey, cuando venia al patio, recordaba al hombre de la jaula al que
                se le daba buena comida, abrigo y buen cuidado, pero ¿porqué no le gustaba? Y el
                psicólogo notó que, mientras al comienzo el hombre permanecía impermeable a las
                afirmaciones del rey, ahora parecía vacilar cada vez más ante el discurso del rey por
                un segundo el odio desaparecía de sus ojos como si estuviera preguntándose a si
                mismo si tal vez no fuera cierto lo que decía el rey.<br>
                Y después de unas pocas semanas más, el hombre comenzó a discutir con el
                psicólogo lo ventajoso que era que a un hombre se le diera alimento y abrigo, y
                cómo el hombre debe de cualquier manera vivir su destino y lo inteligente que era
                aceptarlo. Pronto desarrolló una extensa teoría acerca de la seguridad y la
                aceptación del destino. Durante ese periodo era muy voluble, hablaba mucho,
                aunque la conversación era más un monólogo. El psicólogo notó que su voz era
                insulsa y falsa, como la voz de los locutores de T.V. que hacen un esfuerzo para
                mirar a los ojos y tratan de parecer sinceros cuando dicen que se debe mirar el
                programa que anuncian o los locutores de la radio a quienes se les paga para
                persuadir de que a uno le debe gustar la música chabacana.<br>
                Y el psicólogo notó también que las comisuras de la boca del hombre estaban
                siempre caídas, como si estuviera muy amargado.<br>
                "¡Qué extraño!" Pensaron los psicólogos, "Y que patético, ¿por qué se preocupa
                tanto por que aprueben su modo de vida.<br>
                En los días siguientes cuando el rey iba al patio, el hombre lo adulaba detrás de las
                rejas de su jaula y le agradecía el alimento y la protección. Pero cuando el rey no
                estaba en el patio y el hombre no se daba cuenta que el psicólogo estaba presente,
                su expresión era muy distinta hosca y malhumorada.<br>
                Cuando el cuidador le alcanzaba la comida, a través de las rejas, el hombre a
                menudo tiraba los platos o vertía el agua y luego se avergonzaba a causa de su
                estupidez y torpeza. Su conversación era ahora cada vez más intrascendente y en
                lugar de las complicadas teorías filosóficas acerca del valor de ser cuidado se
                redujeron a simples frases que repetía incesantemente "es el destino': "así es El
                psicólogo se sorprendía al encontrar que el hombre estaba ahora tan torpe como
                para tirar su comida, o tan estúpido como para pronunciar esas áridas frases, pues
                sabía por sus test que el hombre originariamente tenía un buen nivel intelectual.
                El hombre de la jaula se dedicó cada vez más a sentarse sencillamente durante todo
                el día al sol, cuando éste pasaba a través de las rejas, y sus únicos movimientos
                eran para cambiar su posición, de tanto en tanto, desde la mañana hasta la noche.
                Era difícil precisar cuándo había comenzado esta última fase. Pero el psicólogo se
                dio cuenta que la cara del hombre no parecía tener ahora ninguna expresión; su
                sonrisa no era ya aduladora, sino simplemente hueca y sin sentido, como los gestos
                que hace un bebe cuando tiene gases en el estómago.<br>
                Y ahora el hombre, en sus inconexas conversaciones, no usaba jamás la palabra
                "yo”.<br>
                Aceptó la jaula. No tenía ira, ni odio, ni racionalizaciones. Pero ahora estaba
                demente. La noche en que el psicólogo se dio cuenta de esto, se sentó en su
                departamento tratando de escribir el informe final. Se fue a la cama sin terminar,
                pero no podía dormir; algo lo carcomía per dentro. Algo que en épocas menos
                racionales y científicas sé llamaría consciencia. "¿Por qué no le dije al rey que éste
                era un experimento que ningún hombre puede hacer o, al menos, porque no grite
                que no tendría nada que ver con todo este sucio asunto?”<br>
                Finalmente se quedó dormido. Más tarde, en las primeras horas de la mañana, un
                sueño sobrecogedor lo despertó. Soñó que una multitud de gente se había reunido
                en el patio y el hombre de. La jaula no más inerte ni vacuo gritaba a través de las
                rejas con apasionada elocuencia: "No es solo a mi a quien privaron de su libertad"
                exclamaba. "Cuando el rey me encierra a mi o a cualquier hombre en una jaula,
                también los priva de libertad a cada uno de ustedes. ¡El rey debe irse! comenzó a
                cantar la gente. "¡El rey debe irse!”.<br>
                Tomaron las rejas de la jaula y, rompiéndolas, las usaron como lanzas mientras
                cargaban contra el palacio.<br>
                "¡Al infierno! dijo el psicólogo saltando fuera de la cama. "Tal vez algunos sueños
                deban ser actuados”<br>
